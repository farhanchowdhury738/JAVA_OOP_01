import java.lang.*;

public class Casting {
    public static void main (String[] args)
    {
        System.out.println("Hello Casting!");

        System.out.println();

        double price = 10.0;
        double final_price  =  price+18;

        System.out.println(final_price);

        int p = 100;
        int f_p = p+ (int) 18.11;
        System.out.println(f_p);

    }
}